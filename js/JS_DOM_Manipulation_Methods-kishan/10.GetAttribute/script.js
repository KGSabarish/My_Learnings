 // Example using getAttribute
 var element = document.getElementById('example');

 // Retrieve the value of the 'class' attribute
 var classAttributeValue = element.getAttribute('class');
 console.log('Class attribute:', classAttributeValue);

 // Retrieve the value of the 'data-info' attribute
 var dataInfoAttributeValue = element.getAttribute('data-info');
 console.log('data-info attribute:', dataInfoAttributeValue);